// 【YB分类整理】问题描述20240726-2 No.85 顺序调整 by baolei end
export default {
  name: 'Login',
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!validUsername(value)) {
        callback(new Error('用户名称长度2-10位'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (!validPassword(value)) {
        callback(new Error('登录密码长度6-18位'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: '',
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {},
      img: '', // 【YB分类整理】问题描述20240726-2 No.85 顺序调整 by baolei
      // 【YB分类整理】问题描述20240726-2 No.85 顺序调整 by baolei start
      indexLogo
      // 【YB分类整理】问题描述20240726-2 No.85 顺序调整 by baolei end
    }
  },
  watch: {
    $route: {
      handler: function (route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  // created() {
  //   this.getCaptcha()
  // },
  created() {
    // 恢复上次输入的值
    const savedData = localStorage.getItem('loginForm');
    if (savedData) {
      this.loginForm = JSON.parse(savedData);
    }
  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
  },
  methods: {
    // getCaptcha() {
    //   captcha().then(response => {
    //       if(response !== undefined){
    //           this.img = response.base64
    //           this.loginForm.key = response.key
    //       }
    //   })
    // },
    // checkCapslock(e) {
    //   const { key } = e
    //   this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z')
    // },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      localStorage.setItem('loginForm', JSON.stringify(this.loginForm));
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm)
            .then(() => {
              this.$notify({
                title: '登录成功',
                // 【YB分类整理】问题描述20240726-2 No.85 顺序调整 by baolei start
                // message: '欢迎您登录后台管理系统',
                message: '欢迎您登录七星智运综合管理系统',
                // 【YB分类整理】问题描述20240726-2 No.85 顺序调整 by baolei end
                type: 'success'
              })
              console.log(this.redirect)
              console.log(this.otherQuery)
              this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
              // this.$router.push({ path: this.redirect })
              this.loading = false
              this.$socket.emit('login', {
                'token': getToken()
              }, function () {
                return true;
              });
            })
            .catch(() => {
              this.loading = false
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
  }
}
</script>

